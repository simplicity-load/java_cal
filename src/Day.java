public class Day {
    private int day;
    private int daySinceBeginning;

    public Day(int day, int daySinceBeginning)
    {
        this.day = day;
        this.daySinceBeginning = daySinceBeginning;
    }

    public int getDay()
    {
        return day;
    }

    public int getDaySinceBegin()
    {
        return day;
    }
}
